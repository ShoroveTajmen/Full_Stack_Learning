{
  //Basic Data Type

  //string
  let firstName: string = "shorove";
  //number
  let roll: number = 123;
  // boolean
  let isAdmin: boolean = true;
  // undefined
  let x: undefined = undefined;
  // null
  let y: null = null;

  // we can write this type as well
  let d: number;
  d = 123;

  // Array

  let friends: string[] = ["srvi", "rohan"];
  let eligibleRollList: number[] = [1, 3];

  //tuple -> it's a kind of array but maintain the order or type of values
  let coordinates: [number, number] = [1, 5];
  let ageName: [number, string, boolean] = [50, "Mr.X", true];
  ageName[0] = 67;
}
